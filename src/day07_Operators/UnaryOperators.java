package day07_Operators;

public class UnaryOperators {

    public static void main(String[] args) {

        int num1= +25;
        int num2= -25;

        System.out.println(num1+num2);

        int b=25;

        System.out.println(b++);
        System.out.println(b--);
        System.out.println(b);
    }
}
