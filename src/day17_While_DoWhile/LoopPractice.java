package day17_While_DoWhile;

public class LoopPractice {

    public static void main(String[] args) {

        // for loop if u know how many times turn

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("-----------------------");

        int j = 1;

        while (j <= 10) {
            j++;
            System.out.println(j);
            System.out.println("osman");
            break;
        }
    }
}
